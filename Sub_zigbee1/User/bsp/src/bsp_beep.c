/*
*********************************************************************************************************
*	函 数 名: bsp_InitBeep
*	功能说明: 
*	形    参: 
*	返 回 值: 
*********************************************************************************************************
*/
void bsp_InitBeep(void)
{
	//1.定义结构体
	GPIO_InitTypeDef GPIO_InitStruct;
	//2.打开时钟
	RCC_APB2PeriphClockCmd(RCC_ALL_BEEP,ENABLE);
	//3.结构体赋值
	GPIO_InitStruct.GPIO_Pin = BEEP_GPIO_PIN;  	  	//引脚（小端口0~15）
	GPIO_InitStruct.GPIO_Mode = GPIO_Mode_Out_PP;	//通用推挽输出
	GPIO_InitStruct.GPIO_Speed = GPIO_Speed_50MHz; 	//输出速度
	//4.GPIO_Init初始化
	
	GPIO_Init(BEEP_GPIO_PORT,&GPIO_InitStruct);
	//5.初始电平设置
	GPIO_ResetBits(BEEP_GPIO_PORT,BEEP_GPIO_PIN);
}

/*
*********************************************************************************************************
*	函 数 名: 
*	功能说明: 
*	形    参: 
*	返 回 值: 
*********************************************************************************************************
*/
void bsp_BeepOn(void)
{
	//位带
	BEEP = 1;
	
}


/*
*********************************************************************************************************
*	函 数 名: bsp_BeepOff
*	功能说明: 控制蜂鸣器关闭
*	形    参: 
*	返 回 值: 
*********************************************************************************************************
*/
void bsp_BeepOff(void)
{
		//库函数
		GPIO_ResetBits(BEEP_GPIO_PORT,BEEP_GPIO_PIN);
}

uint8_t bsp_IsBeepOn(void)
{
	if((BEEP_GPIO_PORT->ODR & BEEP_GPIO_PIN) == BEEP_GPIO_PIN)
	{
		return 1;
	}
	return 0;
}

/*
*********************************************************************************************************
*	函 数 名: 
*	功能说明: 
*	形    参: 
*	返 回 值: 
*********************************************************************************************************
*/

void bsp_beepToggle(void)
{
	BEEP_GPIO_PORT->ODR ^= BEEP_GPIO_PIN;
}

